package Model;

public class Product {
	
	private int id;
	private String name;
	private int price;
	private String image;
	
	
	public Product(int id, String name, int price, String image) {
		this.id = id;
		this.name = name;
		this.price = price;
		this.image = image;
	}
	
	
	

}
